1)  BIDANG PERENCANAAN PEMBANGUNAN
1.   Menampung aspirasi dari masyarakat terkait pengajuan-pengajuan kegiatan dalam pembangunan kemasyarakatan,
2. Menyusun pengajuan perencanaan kegiatan pembangunan masyarakat melalui musyawarah desa,(Musdes) dan Musrembang.
3. Mengajukan  rencana  kegiatan-  kegiatan   yang  bersifat  pemberdayaan kemasyarakatan demi meningkatkan Sumber Daya Manusia (SDM)  yang tepat guna,
4.   Berkoordinasi,  konsultasi  terhadap  gagasan-gagasan  yang  bersifat  perencanaan dalam suatu kegiatan pengembangan, pembangunan kemasyarakatan,
5.   Sinergisitas antara LPM dan aparatur pemerintahan pada umum khususnya di desa
Sumur Bandung.
6.   Mengawal kebijakan-kebijakan terkait perncanaan yang bersipat kontinu.
7. Menyusun   rencana,   melaksanakan,   mengendalikan,   melestarikan   dan mengembangkan hasil pembangunan secara partisipatif.


2)  BIDANG PELAKSANAAN PEMBANGUNAN
1.   Membantu pelaksanaan program-program di bidang pembangunan desa baik yang bersumber daya alam (SDA) maupun sumber daya manusia (SDM).
2.   Melaksanakan tata ruang desa yang teratur dan rapih.
3.   infrastruktur desa sesuai dengan kondisi di wilayah setempat.
4.   Memfasilitasi pelaksanaan musyawarah pembangunan desa;
5.   Mengawal terselenggaranya kegiatan yang bersifat pembangunan.


3)  BIDANG KEMASYARAKATAN DAN PEMBERDAYAAN
1.   Menyusun, data-data masyarakat tentang pemberdayaan masyarakat
2.   Memberikan  arahan  dan  menyampaikan  kepada  masyarakat  umum  terkait potensi-potensi pemberdayaan di lingkungan yang bersifat menyeluruh, dan kontinu.
3.   Menampung aspirasi warga bila muncul suatu gagasan atau ide yang bersifat pemberdayaan SDM.
4.   Membuat suatu gagasan pembudidayaan ikan, ternak, atau bercocok tanam.
5. Melakukan upaya peningkatan produtivitas peternakan, seni budaya, sanggar pendidikan, budidaya, dan pelatihan
6.   Merencanakan   dan   melaksanakan   pembangunan   dalam   rangka   perbaikan prasarana dan sarana pemasaran 

7.   pelatihan usaha ekonomi, pertanian, perikanan dan perdagangan
8.   pelatihan teknologi tepat guna
9.   pembentukan  kelompok-kelompok  usaha  produktif  yang  disesuaikan  dengan kondisi desa.


4)  BIDANG PENGENDALIAN PEMBANGUNAN
1.   Menyusun rencana, program kerja dan kegiatan untuk rencana anggaran satuan kerja Bagian Pengendalian Pembangunan,
2.   Mempersiapkan bahan  perumusan kebijakan, pedoman dan petunjuk teknis di
bidang kegiatan pembangunan fisik dan non fisik.
3.   Mewujudkan   mekanisme   pelaksanaan   pembangunan   yang   efektif,   efisien, transparan dan akuntabel,
4.   Mewujudkan koordinasi dan kerjasama dengan lembaga pemerintah dan swasta
dalam pelaksanaan pembangunan,
5.   Melibatkan dan menginformasikan hasil-hasil pembangunan kepada masyarakat.


5)  BIDANG PENGEMBANGAN PENDIDIKAN DAN PELATIHAN
1.   Menyusun dan mengembangkan aspirasi masyarakat terhadap kebudayaan dan kegiatan masyarakat yang terdapat di desa.
2.   Membuat perencanaan pendidikan, pelatihan yang dibutuhkan.
3.   Menginventaris bahan kebuthan pendidikan dna pelatihan.
4.   Membantu meningkatkan kegiatan di bidang pendidikan formal dan non formal di desa
5.   Memberikan   bimbingan   dan   pembinaan   terhadap   kelompok   usaha   kecil menengah, terutama dengan memberikan kursus-kursus pelatihan.
6.   Melaksanakan kegiatan peningkatan pengetahuan dan keterampilan terutama bagi
para remaja putus sekolah
7.   Membuat kelompok pendidikan kejar paket. A,B.C
8.   Melatih, mendidik, memberikan bimbingan terhadap calon pencari kerja.


6)  BIDANG KEMITRAAN DAN ANTAR LEMBAGA
1.   Menyusun kegiatan  yang bersifat kemitraan, baik dengan pemerintah,  swasta, perusahaan ataupun lembaga-lembaga terkait,
2.   Melaksanakan koordinasi dengan pihak-pihak terkait, baik pemerintah, swasta
atau lembaga.
3.   Membuat  peraturan-peraturan  organisasi  yang  sesuai  dan  tidak  bertentangan dengan peraturan yang ada. 

4.   Membuat MoU kemitraan dengan pihak perusahaan yang bergerak di wilayah
Desa Sumur Bandung.
5.   Meningkatkan kesadaran masyarakat terhadap hukum dan perundang-undangan yang berlaku.
6.   Mengevaluasi pelaporan kegiatan di bidang kemitraan.
7.   Melaksanakan tugas yang diberikan atasan dengan penuh tanggung jawab dengan mengutamakaan koordinasi.


7)  BIDANG PEMUDA DAN OLAH RAGA
1.   Menyusun   program   kerja   bidang   yang   berkaitan   dengan   pemudan   dan keolahragaan.
2.   Menampung aspirasi, gagasan untuk mengembangkan suatu kegiatan dibidang kepemudaan.
3.   Memberikan pembinaan terhadap pemuda, baik segi pendidikan, pelatihan, dan olahraga.
4.   Membentuk team asuhan pembinaan, baik sebi budaya, olahragapembinaan, dan pendidikan pelatihan
5.   Membuat kegiatan tournament olahraga dari masing-masing bidang kejuaraan.
6. Membantu usaha pemerintah dalam mencegah dan menanggulangi kenakalan remaja denga mengisi waktu luang untuk pengembangan bakat
7.   Memberikan pembinaan terhadap pemuda agar terhindar dari bahaya Narkotika, narkoba, napza, dan mengalihkan kegiatan yang bersifat positif.
8.   Melibatkan pemuda semaksimal mungkin pada kegiatan pembangunan desa.


8)  BIDANG KEAGAMAAN
1.   Menyusun kegiatan program keagamaan yang bersifat menyeluruh
2.   Mengkoordinasikan berbagai kegiatan partisipasi masyarakat menyangkut usaha- usaha peningkatan kegiatan keagamaan.
3.   Membantu program pembinaan perkawinan sesuai dengan ketentuan perundang- undangan yang berlaku.
4.   Mengarahkan  penggunaan  hasil  zakat,  infak  dan  shodaqoh  serta bantuan  lain untuk fakir miskin dan keperluan pembangunan serta meningkatkan sarana pendidikan sesuai dengan tuntutan agama.
5.   Mengisi hari raya keagamaan secara terarah, menuju kesadaran toleransi umat beragama.
6. peningkatan  usaha-usaha  pembangunan  khususnya  pembangunan  sarana keagamaan. 

7.   Membantu  mengawal  kebijakan-kebijakan  pemerintah  tentang  kegiatan  yang bertentangan dengan norma-norma agama.
8.   Memberikan pembinaan terhadap masyarakat tentang   kerukunan   antar umat beragama.
9.   Meningkatkan   akses   dan   kualitas   pendidikan   umum   berciri   khas   agama,
pendidikan agama pada satuan pendidikan umum, dan pendidikan keagamaan.


