IV.      BENDAHARA
a.   Tugas :
1.   Menyelenggarakan  administrasi  keuangan  dan  menerima,  menyimpan serta menyerahkan uang/ surat berharga dan barang,
2.   Membantu ketua dalam menyelenggarakan adminitrasi keuangan program kerja LPM, dan
3.   Menyusun Laporan semester dan tahunan keuangan LPM.
b.  Wewenang :
1.   Membuat  sistem  informasi  manajemen  keuangan  yang  dapat  diakses dengan mudah,
2.   Membuat sistem dokumentasi, penyelamatan, keuangan LPM, dan
3.   Membuat perencanaan keuangan LPM.
c.   Fungsi :


1. Menyelenggarakan pembukuan, penyusunan laporan keuangan dan penyimpanan keuangan,
2. Mengadakan pencatatan swadaya gotong royong masyarakat dalam pembangunan yang dinilai dengan uang, dan
3.   Melaksanakan tugas tertentu yang diberikan oleh ketua. 

